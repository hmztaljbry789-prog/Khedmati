// Converts free-text duration strings (e.g. "60 mins", "1 hr 30 mins") into
// localized text. Falls back to the raw value for unrecognized formats.
export function formatDuration(time, locale = "en") {
    if (time === null || time === undefined) return "";
    const raw = String(time).trim();
    if (!raw) return "";
    if (locale !== "ar") return raw;
    // normalize a known data typo like "40 0mins"
    const cleaned = raw.replace(/0mins/gi, " mins");
    let hours = 0;
    let mins = 0;
    let matched = false;
    const hrMatch = cleaned.match(/(\d+)\s*(?:hrs?|hours?)/i);
    if (hrMatch) {
        hours = parseInt(hrMatch[1], 10);
        matched = true;
    }
    const minMatch = cleaned.match(/(\d+)\s*(?:mins?|minutes?)/i);
    if (minMatch) {
        mins = parseInt(minMatch[1], 10);
        matched = true;
    }
    if (!matched) return raw;
    const unit = (n, one, two, few, many) => {
        if (n === 1) return one;
        if (n === 2) return two;
        if (n >= 3 && n <= 10) return n + " " + few;
        return n + " " + many;
    };
    const parts = [];
    if (hours) parts.push(unit(hours, "\u0633\u0627\u0639\u0629", "\u0633\u0627\u0639\u062a\u0627\u0646", "\u0633\u0627\u0639\u0627\u062a", "\u0633\u0627\u0639\u0629"));
    if (mins) parts.push(unit(mins, "\u062f\u0642\u064a\u0642\u0629", "\u062f\u0642\u064a\u0642\u062a\u0627\u0646", "\u062f\u0642\u0627\u0626\u0642", "\u062f\u0642\u064a\u0642\u0629"));
    return parts.join(" \u0648");
}
